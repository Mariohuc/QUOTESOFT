    private void BotonGuardarCiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarCiaActionPerformed
        if( !DatosValidos() ){
            stateCiaOperationjLabel.setText("Â¡FallÃ³ al guardar! Datos incorrectos");
            return;
        }
        FileGeneradorCodigos file = new FileGeneradorCodigos();
        file.escribir(nuevo);
        
        CiaCodContent = CodeGenerate.getCode(Entities.CIAS);
        PREPARAR_DECLARACION.agregarCia(CiaCodContent,
            this.CajaNomCia.getText(),
            this.CajaRuc.getText(),
            "A");

        String [] datos = new String[4];
        datos[0] = String.valueOf(CiaCodContent);
        datos[1] = CajaNomCia.getText();
        datos[2] = CajaRuc.getText();

        limpiar();
        modelo.addRow(datos);
        stateCiaOperationjLabel.setText("Exito al guardar");

    }//GEN-LAST:event_BotonGuardarCiaActionPerformed

    private void BotonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarActionPerformed
        if( !DatosValidos() )
            return;
        // TODO add your handling code here:
        PREPARAR_DECLARACION.ActualizarCia(CiaCodContent,
            this.CajaNomCia.getText(),
            this.CajaRuc.getText(),
            "A");
        modelo.setValueAt(CajaNomCia.getText(), getFilaSeleccionada(), 1 );
        modelo.setValueAt(CajaRuc.getText(), getFilaSeleccionada(), 2 );
        limpiar();
        
        BotonGuardarCia.setEnabled(true);
        BotonActualizar.setEnabled(false);
    }//GEN-LAST:event_BotonActualizarActionPerformed

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int fila = TABLA_MOSTRAR_CIAS.getSelectedRow();
        if(fila >= 0){
            CiaCodContent = (int) TABLA_MOSTRAR_CIAS.getValueAt(fila, 0);
            CajaNomCia.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 1).toString());
            CajaRuc.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 2).toString());

            BotonGuardarCia.setEnabled(false);
            BotonActualizar.setEnabled(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "No se selecciono fila");
        }
        setFilaSeleccionada( fila );
    }//GEN-LAST:event_jMenuItem1ActionPerformed

