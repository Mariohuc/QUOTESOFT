    public boolean DatosValidos( ){
        boolean flag1, flag2,flag3;
        flag1 = flag2 = flag3 = true;
        if( !checkNum( String.valueOf( CiaCodContent ) ) ){ 
            flag1 = false;
        }
        if( !checkAlpha(CajaNomCia.getText()) ){
            CajaNomCia.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag2 =  false;
        }
        if( !checkNum(CajaRuc.getText()) || CajaRuc.getText().length() != 11  ){
            CajaRuc.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag3 = false;    
        }
        return ( flag1 && flag2 && flag3);
    }
    
    public boolean checkAlpha(String cadena) {
	boolean respuesta = false;
	if ((cadena).matches("([a-z]|[A-Z]|\\s|\\.)+")) {
            respuesta = true;
	}
        return respuesta;	
    }
    public boolean checkNum(String str){ 
        boolean respuesta = false; 
        if ((str).matches("([0-9])+")) { 
            respuesta = true; 
        } 
        return respuesta; 
    }

