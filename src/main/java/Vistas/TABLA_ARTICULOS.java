    public boolean DatosValidos( ){
        boolean flag1, flag2,flag3, flag4, flag5;
        flag1 = flag2 = flag3 = flag4 = flag5 = true;
        if( !checkNum(CajaCodigoArt.getText()) ){ 
            CajaCodigoArt.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag1 = false;
        }
        if( !checkAlphayNumber(CajaDescripcionArt.getText()) ){
            CajaDescripcionArt.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag2 =  false;
        }
        if( !checkNumDecimal(CajaPrecUnit.getText())  ){
            CajaPrecUnit.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag3 = false;    
        }
        if( !checkNum(CajaCiaCodigo.getText()) ){ 
            CajaCiaCodigo.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag4 = false;
        }
        if( !checkNum(CajaSucCodigo.getText()) ){ 
            CajaSucCodigo.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
            flag5 = false;
        }
        return ( flag1 && flag2 && flag3 && flag4 && flag5 );
    }
    
    public boolean checkAlpha(String cadena) {
	boolean respuesta = false;
	if ((cadena).matches("([a-z]|[A-Z]|\\s)+")) {
            respuesta = true;
	}
        return respuesta;	
    }
    public boolean checkAlphayNumber(String cadena) {
	boolean respuesta = false;
	if ((cadena).matches("([a-z]|[A-Z]|[0-9]|\\s|\\.|\\-)+")) {
            respuesta = true;
	}
	return respuesta;
    }
    public boolean checkNum(String str){ 
        boolean respuesta = false; 
        if ((str).matches("([0-9])+")) { 
            respuesta = true; 
        } 
        return respuesta; 
    }
    public boolean checkNumDecimal( String str ){
        boolean respuesta = false; 
        if ((str).matches("([0-9]|\\.)+")) { 
            respuesta = true; 
        } 
        return respuesta;
    }
    
    
   
    

