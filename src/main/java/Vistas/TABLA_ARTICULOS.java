    private void BotonActualizarArtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonActualizarArtActionPerformed
        if( !DatosValidos() )
        return;
        // TODO add your handling code here:
        PREPARAR_DECLARACION.ActualizarArticulo( Integer.parseInt( this.CajaCodigoArt.getText()), 
                this.CajaDescripcionArt.getText(),
                Double.parseDouble( this.CajaPrecUnit.getText() ),
                "A",
                Integer.parseInt( this.CajaSucCodigo.getText()),
                Integer.parseInt( this.CajaCiaCodigo.getText()));
        
        modelo.setValueAt(CajaDescripcionArt.getText(), getFilaSeleccionada(), 1 );
        modelo.setValueAt(CajaPrecUnit.getText(), getFilaSeleccionada(), 2 );
        modelo.setValueAt(CajaSucCodigo.getText(), getFilaSeleccionada(), 3 );
        modelo.setValueAt(CajaCiaCodigo.getText(), getFilaSeleccionada(), 4 );
        
        limpiar();
        CajaCodigoArt.setEnabled(true);
        BotonGuardarArt.setEnabled(true);
        BotonActualizarArt.setEnabled(false);
    }//GEN-LAST:event_BotonActualizarArtActionPerformed

    private void BotonGuardarArtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarArtActionPerformed
        if( !DatosValidos() )
        return;
        FileGeneradorCodigos file = new FileGeneradorCodigos();
        file.escribir(nuevo);

        PREPARAR_DECLARACION.agregarArticulo(Integer.parseInt( this.CajaCodigoArt.getText()),
            this.CajaDescripcionArt.getText(),
            Double.parseDouble( this.CajaPrecUnit.getText() ),
            "A",
            Integer.parseInt( this.CajaSucCodigo.getText()),
            Integer.parseInt( this.CajaCiaCodigo.getText()));

        String [] datos = new String[5];
        datos[0] = CajaCodigoArt.getText();
        datos[1] = CajaDescripcionArt.getText();
        datos[2] = CajaPrecUnit.getText();
        datos[3] = CajaSucCodigo.getText();
        datos[4] = CajaCiaCodigo.getText();

        limpiar();
        modelo.addRow(datos);

    }//GEN-LAST:event_BotonGuardarArtActionPerformed

