    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        int fila = TABLA_MOSTRAR_CIAS.getSelectedRow();
        if(fila >= 0){
            CajaCodigoArt.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 0).toString());
            CajaDescripcionArt.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 1).toString());
            CajaPrecUnit.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 2).toString());
            CajaCiaCodigo.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 4).toString());
            CajaSucCodigo.setText(TABLA_MOSTRAR_CIAS.getValueAt(fila, 3).toString());
            CajaCodigoArt.setEnabled(false);
            BotonGuardarArt.setEnabled(false);
            BotonActualizarArt.setEnabled(true);
        }
        else{
            JOptionPane.showMessageDialog(null, "No se selecciono fila");
        }
        setFilaSeleccionada( fila );
    }//GEN-LAST:event_jMenuItem1ActionPerformed

